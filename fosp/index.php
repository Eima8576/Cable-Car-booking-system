            <h2>Cable</h2>
            <p>The cable car has helped in covering the distance to the Temple in just 10 minutes, which would otherwise be a five-hour uphill climb. The ride begins from across the Trishuli River bank (258 meter) and as you ascend the hill the view of surrounding hills and dense forest is mesmerizing.
                Safety and keeping it green are the top most priorities of the company. The cable car is maintained every day before and after normal operation hours. As per the Doppelmayr protocol, the cable car takes a break for 2-3 days in four month intervals.
                
                The top station is situated close to the temple in 1302 meter. Manakamana Cable Car station is 2 1⁄2 Hrs drive from Kathmandu, 2 Hrs from Pokhara and, 1 Hour from Narayanghat.
             <ul>
                 <li>Cable car covers the distance of 3 KM in just 10 minutes.</li>
                 <li>Each cabin accommodates 6 passengers.</li>
                 <li>Overall, the cable car can system handle 660 people per hour</li>
                 <li>The route is designed specially to enable use by differently abled pilgrims</li>
